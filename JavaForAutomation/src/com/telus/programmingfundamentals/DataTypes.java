package com.telus.programmingfundamentals;

public class DataTypes {

	public static void main(String[] args) {

		// we have two type of data type
		/* 1 Primitive datatype- It contains 
		 * Boolean-boolean
		 * Character- char
		 * Integer-byte,short,int,long
		 * Floating Point- float,double
		 * 
		 *  2 Non Primitve data type
		 *  String
		 *  Arrays
		 *  classes
		 */

		//   boolean

		boolean TF=10>5;
		System.out.println("The answer is : "+TF);	


		//  Char
		char c='a';
		System.out.println("The value of char is :" +c);		

		//  int , byte ,short ,long

		int empId=7865763;
		System.out.println("My Emp Id is :"+empId); 
		
		byte date=15;
		System.out.println("Date is : "+date);


		long panCard=9821343210L;
		System.out.println("Pan Card Number is :"+panCard);

		float marks=85.34f;
		System.out.println("Student got "+marks+"%");

		short TV=25000;
		System.out.println("The  Cost of Tv is :" +TV);


		double accurateValue=67.2365349865238745;
		System.out.println("Accuracy of machine is : "+accurateValue);

		// String

		String myName="Pallav";
		System.out.println("My name is  : "+myName);




	}

}
